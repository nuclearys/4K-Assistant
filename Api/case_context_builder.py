def _build_signal_driven_situations(
    *,
    domain_family: str,
    case_type_code: str | None,
    profile_tasks: list[str],
    profile_risks: list[str],
    profile_artifacts: list[str],
    case_specificity: dict[str, Any] | None,
) -> list[dict[str, Any]]:
    specificity = case_specificity or {}
    family = str(domain_family or "").strip().lower() or "generic"
    signal_titles = cleanup_case_list(
        [
            *cleanup_case_list(specificity.get("ticket_titles"), limit=3),
            *cleanup_case_list(specificity.get("work_items"), limit=3),
            str(specificity.get("request_type") or ""),
            str(specificity.get("bottleneck") or ""),
        ],
        limit=7,
    )
    signal_sources = cleanup_case_list(
        [
            *cleanup_case_list(specificity.get("data_sources"), limit=3),
            str(specificity.get("source_of_truth") or ""),
            *profile_artifacts,
        ],
        limit=4,
    )
    current_state = cleanup_case_text(str(specificity.get("current_state") or "")).strip()
    if not signal_titles and current_state:
        signal_titles = [current_state.split(".")[0].strip()]
    if not signal_titles:
        signal_titles = cleanup_case_list(profile_tasks, limit=5)
    if not signal_titles:
        return []

    derived: list[dict[str, Any]] = []
    seen_titles: set[str] = set()
    for idx, raw_title in enumerate(signal_titles[:7], start=1):
        title = _compact_signal_phrase(raw_title)
        if not title:
            continue
        title_key = title.lower()
        if title_key in seen_titles:
            continue
        seen_titles.add(title_key)
        lower_title = title.lower()
        if current_state and lower_title in current_state.lower():
            problem_event = current_state
        else:
            problem_event = f"по ситуации «{lower_title}» нет подтвержденного следующего шага или полного статуса"
        source_keywords = [part.lower()[:24] for part in signal_sources[:2] if part]
        keywords = tuple(
            cleanup_case_text(item).lower()[:24]
            for item in [*title.split()[:3], *source_keywords]
            if cleanup_case_text(item)
        )
        derived.append(
            {
                "situation_code": f"{family}_signal_{idx}",
                "incident_title": title,
                "keywords": keywords,
                "problem_event": cleanup_case_text(problem_event),
                "expected_step": _default_signal_expected_step(case_type_code, title, family=family),
                "risk": _default_signal_risk(family, profile_risks, title),
                "constraint": _default_signal_constraint(family, cleanup_case_list(specificity.get("constraints"), limit=2)),
                "source_of_truth": ", ".join(signal_sources[:2]),
            }
        )
    return derived


