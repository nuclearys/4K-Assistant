def _cleanup_incident_title_candidate(text: str) -> str:
    clean = cleanup_case_text(str(text or "")).strip().strip("«»\"'")
    clean = clean.rstrip(".")
    clean = clean.replace("**", "").strip()
    if not clean:
        return ""
    if clean[:1].islower():
        clean = clean[:1].upper() + clean[1:]
    return clean


def _derive_incident_title(
    *,
    case_type_code: str | None,
    situation: dict[str, Any],
    problem_event: str,
    process: str,
    ticket_titles: list[str],
    current_state: str,
    case_specificity: dict[str, Any] | None,
) -> str:
    type_code = str(case_type_code or "").upper()
    specificity = case_specificity or {}

    for candidate in (
        situation.get("incident_title"),
        specificity.get("idea_label") if type_code == "F10" else "",
        ticket_titles[0] if ticket_titles else "",
        specificity.get("request_type") if type_code == "F02" else "",
        specificity.get("bottleneck"),
        problem_event,
        specificity.get("_case_title"),
        process,
    ):
        title = _cleanup_incident_title_candidate(str(candidate or ""))
        if not title:
            continue
        if title.lower() in {"рабочая ситуация требует решения", "рабочая ситуация требует уточнения"}:
            continue
        return title

    current = _cleanup_incident_title_candidate(current_state)
    if current:
        current = current.replace("Сейчас ", "").replace("сейчас ", "")
        if len(current) > 96:
            current = current[:96].rsplit(" ", 1)[0].rstrip(",;:-")
        return current.rstrip(".")

    return "Рабочая ситуация требует уточнения"


def _compact_signal_phrase(text: str) -> str:
    clean = cleanup_case_text(str(text or "")).strip().strip("«»\"'")
    if not clean:
        return ""
    clean = clean.rstrip(".")
    clean = clean[:140].rsplit(" ", 1)[0].rstrip(",;:-") if len(clean) > 140 else clean
    if clean[:1].islower():
        clean = clean[:1].upper() + clean[1:]
    return clean


