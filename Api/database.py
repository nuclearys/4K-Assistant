def _build_case_level_prompt_rule(
    *,
    case_id_code: str,
    type_code: str,
    title: str,
    context_domain: str | None,
    trigger_details: str | None,
    intro_context: str | None,
    task_for_user: str | None,
    personalization_variables: str | None,
) -> dict[str, Any]:
    intro = str(intro_context or "").strip()
    task = str(task_for_user or "").strip()
    trigger = str(trigger_details or "").strip()
    placeholders = _extract_case_template_placeholders(f"{intro}\n{task}")
    personalization_codes = _extract_case_personalization_codes(personalization_variables)
    preserve_signals: list[str] = []
    lowered_intro = intro.lower()

    if "срок" in lowered_intro or "sla" in lowered_intro:
        preserve_signals.append("обещанный срок или ориентир по времени должен остаться в тексте")
    if "клиент" in lowered_intro or "заказчик" in lowered_intro:
        preserve_signals.append("в тексте должен оставаться адресат ситуации: клиент, заказчик или иной внешний участник")
    if "следующ" in lowered_intro:
        preserve_signals.append("следующий шаг должен быть виден как часть конфликта, а не теряться в абстрактной формулировке")
    if "не видит" in lowered_intro or "не получил" in lowered_intro:
        preserve_signals.append("разрыв между внутренней работой и внешним восприятием ситуации должен быть сохранен")
    if "эскал" in lowered_intro:
        preserve_signals.append("если в исходном шаблоне есть эскалация, она должна остаться в ограничениях или в задаче")
    if "ресурс" in lowered_intro or "команд" in lowered_intro or "рол" in lowered_intro:
        preserve_signals.append("ресурсные ограничения, роли и координация не должны схлопываться в один локальный инцидент")
    if "иде" in lowered_intro:
        preserve_signals.append("идея, критерии оценки или генерация идей не должны подменяться ответом клиенту или общим действием")
    if "разговор" in lowered_intro or "бесед" in lowered_intro:
        preserve_signals.append("разговорный формат кейса должен сохраняться как разговор, а не как общий план действий")

    placeholder_concretization_rules = _build_placeholder_concretization_rules(
        type_code=type_code,
        placeholders=placeholders,
        personalization_codes=personalization_codes,
    )

    return {
        "case_id_code": case_id_code,
        "type_code": type_code,
        "title": title,
        "context_domain": str(context_domain or "").strip(),
        "trigger_details": trigger,
        "intro_template": intro,
        "task_template": task,
        "placeholders": placeholders,
        "personalization_field_codes": personalization_codes,
        "placeholder_concretization_rules": placeholder_concretization_rules,
        "preserve_signals": preserve_signals,
        "task_style_hint": task,
        "readability_requirements": {
            "spelling_must_be_correct": True,
            "sentences_should_be_short": True,
            "long_lists_in_intro_forbidden": True,
            "case_should_be_easy_to_read": True,
            "avoid_heavy_bureaucratic_phrasing": True,
        },
    }

